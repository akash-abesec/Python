"""
Operator           Name               Example
  +               Addition             x+y
  -               Subtraction          x-y
  *               Multiplication       x*y
  /               Division             x/y
  %               Modulus              x%y
  **              Exponents            x**y
  //              Floor division       x//y
"""
a=10
b=20
print(a+b)
print(b-a)
print(a*b)
print(b/a)      #it always return a float type with one decimal point
print(b%3)      #retuns the remainder


print(5**3)     #it means 5*5*5=125 "square of 5"
print(10//3)    #it returns the floor value means integer value only 3 not 3.33333333
print(12//3)
