"""
Operator                      Name                         Example
==                            Equal                         x==y
!=                            Not equal                     x!=y
>                             Greater than                  x>y
<                             Less than                     x<y
>=                            Greater than or equal to      x>=y
<=                            Less than or equal to         x<=y
"""
x=10
y=10
print(x==y)

z=23
print(x==z)

print(x!=z)
print(x>y)
print(x<z)
print(x>=z)
print(x<=z)