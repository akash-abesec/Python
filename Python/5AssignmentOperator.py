"""
Operator                 Example                     Same as
=                         x=5                         x=5
+=                        x+=3                        x=x+3
-=                        x-=3                        x=x-3
"""


x=5
print(x)                      #assign the value of right side to the left side

x=x+5
print(x)

x+=5
print(x)

x=x-5
print(x)

x-=5
print(x)